# Creating Lists

fruits = ["Apple", "Banana", "Mango", "Orange"]

print("List:", fruits)

# Accessing Elements

print("First Fruit:", fruits[0])
print("Second Fruit:", fruits[1])
print("Last Fruit:", fruits[-1])

# Updating Elements

fruits[1] = "Grapes"

print("Updated List:", fruits)

# List Length

print("Total Fruits:", len(fruits))